package com.phpsysinfo.psiparser;

public class PSIParserException extends Exception {

  public PSIParserException() {
    super();
  }

  public PSIParserException(String message) {
    super(message);
  }

  public PSIParserException(Throwable cause) {
    super(cause);
  }

  public PSIParserException(String message, Throwable cause) {
    super(message, cause);
  }

}
